// | (Bitwise OR) -> Boolean OR

// -----Assignment Operators - 5-----
// = -> Simple Assigning values
// += -> Adds the right operand to left operand and assins to the left operand.
// -= -> Substract the right operand from left operand and assins to the left operand.
// *= -> Mutiplies the right operand to left operand and assins to the left operand.
// /= -> Divide the right operand to from operand and assins to the left operand.

// -----Conditional Operators-----
// ? ->  test ? epression 1 : expression 2;

// Test − refers to the conditional expression

// expr1 − value returned if the condition is true

// expr2 − value returned if the condition is false

let num = -2;
let result = num > 0 ? "positive" : "non-positive";
console.log(result);
