// % -> Modulus
// ++ -> Increment
// -- -> Decrement

// -----Relational Operators - 6-----
// > -> Greter than
// < -> Lesser than
// >= -> Greter than or Equal
// <= -> Lesser than or Equal
// == -> Equal
// != -> Not Equal

// -----Logical Operators - 3-----
// && (AND) -> must true all expressions
// || (OR) -> at least one expressions gonna be true
// !(NOT) -> Expressions in parantheses is false gives true

// -----Bitwise Operators - 2-----
